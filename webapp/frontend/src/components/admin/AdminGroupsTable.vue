<template>
  <div>
    <a v-if="hasLongItems" class="link-toggle-read-all" @click="toggleReadAll">{{ !readAll ? "Read all" : "Read less" }}</a>
    <v-data-table
      :headers="table.headers"
      :items="data"
      :sort-by="'groupId'"
      :sort-desc="true"
      class="elevation-1">

      <!-- Actions -->
      <template v-slot:item.actions="{item}">
        <a class="link-action" @click="emitEditGroup(item.groupId)">Edit Group</a>
        <a class="link-action" @click="emitRemoveGroup(item.groupId)">Remove Group</a>
      </template>
    </v-data-table>
  </div>
</template>

<script>
  import { DisplayTime } from '/src/helpers/time.js'

  export default {
    name: 'AdminGroupsTable',
    props: {
      propItems: {
        type: Array,
        required: true,
      }
    },
    data: () => ({
      data: [],
      readAll: false,
      hasLongItems: false,
      table: {
        headers: [
          { text: 'Group ID', value: 'groupId' },
          { text: 'Name', value: 'name' },
          { text: 'Created At', value: 'createdAt' },
          { text: 'Updated At', value: 'updatedAt' },
          { text: 'Actions', value: 'actions' },
        ],
      }
    }),
    mounted () {
      this.data = this.propItems
    },
    methods: {
      emitEditGroup(groupId) {
        this.$emit('emitEditGroup', groupId)
      },
      emitRemoveGroup(groupId) {
        this.$emit('emitRemoveGroup', groupId)
      },
      toggleReadAll() {
        this.readAll = !this.readAll;
      },
      getText(text) {
        if (this.readAll) return text;
        else {
          if (!this.hasLongItems) this.hasLongItems = true;
          return text.slice(0,10) + "...";
        }
      },
      parseTime(timestamp) {
        return DisplayTime(timestamp)
      },
    },
    watch: {
      propItems: {
        handler(newVal) {
          this.data = newVal
        },
        immediate: true,
      },
    },
  }
</script>

<style scoped lang="scss">
